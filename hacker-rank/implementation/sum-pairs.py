def divisibleSumPairs(n, k, ar):
    # Write your code here
    
    arsoma = []
    for index, item in enumerate(ar):
        soma = 0
        for cont in range(n-1):
            soma = item + ar[cont+index+1]
            arsoma.append(soma)
        n -= 1
    
    result = sum(1 for i in arsoma if i % k == 0)
    
    return result
    
    

